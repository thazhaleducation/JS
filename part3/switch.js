// Series of execution
var dayOfWeek = 1;

switch (dayOfWeek) {
  case 1:
    console.log("SUNDAY");
  case 2:
    console.log("MONDAY");
  case 3:
    console.log("TUESDAY");
  case 4:
    console.log("WEDNESDAY");
  case 5:
    console.log("THURSDAY");
  case 6:
    console.log("FRIDAY");
  case 7:
    console.log("SATURDAY");
  default:
    console.log("WROND DAY OF WEEK");
}
// variables assignment
var a = 1;
var b = 2;

// Arithmetic
console.log("Firstname " + "Surname");
console.log("a + b -->" + (a + b));
console.log("a * 2 -->" + (a * 2));

// Comparison
console.log("a == b -->" + (a == b));
console.log("a < b -->" + (a < b));
console.log("a != b -->" + (a != b));

// Logical
console.log("true && false -->" + (true && false));
console.log("true || false -->" + (true || false));

// Negation
console.log("!true -->" + (!true));
console.log("!(a < b) -->" + !(a < b));

// Increment/Decrement
console.log("++a -->" + (++a));
console.log("b-- -->" + (b--));

